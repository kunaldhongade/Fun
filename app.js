const bettingPool = new web3.eth.Contract(bettingPoolABI, bettingPoolAddress);
const flowUtilityToken = new web3.eth.Contract(flowUtilityTokenABI, flowUtilityTokenAddress);
const topShot = new web3.eth.Contract(topShotABI, topShotAddress);

async function depositEther() {
  const accounts = await web3.eth.getAccounts();
  const amount = document.getElementById("depositAmount").value;
  await flowUtilityToken.methods.deposit().send({ from: accounts[0], value: web3.utils.toWei(amount, 'ether') });
  alert("Deposit successful");
}

async function purchaseNFT() {
  const accounts = await web3.eth.getAccounts();
  const momentID = document.getElementById("momentID").value;
  await bettingPool.methods.purchaseMoment(momentID).send({ from: accounts[0] });
  alert("NFT purchase successful");
}

async function checkBalance() {
  const accounts = await web3.eth.getAccounts();
  const balance = await flowUtilityToken.methods.balanceOf(accounts[0]).call();
  document.getElementById("balanceResult").innerText = `Balance: ${web3.utils.fromWei(balance, 'ether')} Ether`;
}
